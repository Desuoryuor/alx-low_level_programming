#include <stdio.h>
/**
 * main - this code prints the Alphabets in Lowercase then Upper case
 * Return: Always 0 (Sucess)
 */

int main(void)
{
	char c;

	char d;

	c = 'a';
	d = 'A';
	while
		(c <= 'z') {
			putchar(c);
			c++;
		}
	while
		(d <= 'z') {
			putchar (d);
			d++;
		}
	putchar('\n');
	return (0);
}
